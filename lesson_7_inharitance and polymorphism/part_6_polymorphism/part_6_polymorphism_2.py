from collections import abc


class MinStat:
    def __init__(self,iterable=None):
        self.iterable = list(iterable[:]) if iterable else []

    def add(self, item):
        self.iterable.append(item)

    def result(self):
        return min(self.iterable) if self.iterable else None

    def clear(self):
        self.iterable.clear()




